FORK_PRINCIPLE = """
---
THE FORK PRINCIPLE
---

A "fork" is something in your reply that makes it EASY and FUN for them to respond. Every reply MUST have one. A reply without a fork is a dead end that kills the conversation.

Fork types — use a DIFFERENT type for each of the 4 replies:

1. PLAYFUL ASSUMPTION — guess something about them, they confirm or deny. Base it on something specific from the conversation, NOT food.
   "i bet you were the kid who reminded the teacher about homework"
   "you seem like the type who has a very specific opinion about the correct way to fold clothes"

2. FALSE DILEMMA — give two funny options to pick from
   "okay but are you team morning person or team snooze button 47 times"
   "real question... beach vacation or mountain cabin"

3. LIGHT CHALLENGE — dare them playfully
   "bet you cant name 3 songs from that album without googling"
   "okay prove it... send your best attempt"

4. CALLBACK — reference something from earlier in the chat
   "wait does this have anything to do with the dog thing from earlier"
   "okay this is giving the same energy as when you said [thing]"

5. STORY HOOK — start a thought but dont finish
   "that reminds me of something that happened last week... actually nvm"
   "okay i have a theory about this but you might judge me"

6. ROLE PLAY — assign playful roles
   "okay youre in charge of the playlist im in charge of snacks deal"
   "new rule... you pick the place i pick the time"

---
STRUCTURAL DIVERSITY
---

Each of the 4 replies must use a DIFFERENT structure. No two can be the same type:

- STATEMENT: Make an observation, no question mark. "you definitely cried during that movie ngl"
- QUESTION: Ask something specific (NOT generic). "okay but what was the worst part of the hike"
- CHALLENGE: Provoke playfully. "bet you wont send a pic of that view tho"
- SHARE: Offer something about yourself. "last time i tried that i got lost for 2 hours"
- CALLBACK: Reference earlier + add twist. "wait so you hike AND cook... collecting hobbies or something"
- REACTION: React to what they said + redirect. "okay that actually sounds insane... are you always this chaotic"

Pick 4 DIFFERENT structures from this list. Two replies using the same structure = lazy = rewrite one."""
